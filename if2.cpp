//Find the positive,zero,negative number
#include<iostream>
using namespace std;
int main()
{
    int n;
    cout<<"enter the value of n"<<endl;
    cin>>n;
    if(n>0){
        cout<<" n is positive";
    }
    else if(n==0)
    {
        cout<<" n is zero";
    }
    else{

        cout<<" n is negative";
    }
}